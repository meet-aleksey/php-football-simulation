<?php

/**
 * Represents football team info.
 */
class Team {

  /**
   * Team name.
   *
   * @var string
   */
  public $Name;

  /**
   * Games count.
   *
   * @var int
   */
  public $Games;

  /**
   * Wins count.
   *
   * @var int
   */
  public $Wins;

  /**
   * Losses count.
   *
   * @var int
   */
  public $Losses;

  /**
   * Draws count.
   *
   * @var int
   */
  public $Draw;

  /**
   * Goals.
   *
   * @var int
   */
  public $Goals;

  /**
   * Missed.
   *
   * @var int
   */
  public $Missed;

}
